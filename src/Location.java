/**
 * The type Location.
 */
public class Location {

	private int x;
	private int y;

	/**
	 * Instantiates a new Location.
	 *
	 * @param x the x
	 * @param y the y
	 */
	public Location(final int x, final int y) {
		this.x = x;
		this.y = y;
	}

	/**
	 * Gets x.
	 *
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * Sets x.
	 *
	 * @param x the x
	 */
	public void setX(final int x) {
		this.x = x;
	}

	/**
	 * Gets y.
	 *
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * Sets y.
	 *
	 * @param y the y
	 */
	public void setY(final int y) {
		this.y = y;
	}
}
